package no_4;

import java.util.Scanner;


public class No_10951 {
	public static void main(String[] args){

		Scanner sc = new Scanner(System.in);

		//int형 변수를 입력받기 때문에 hasNextIn()를 사용..
		while(sc.hasNext()) {
			int a = sc.nextInt();
			int b = sc.nextInt();

			System.out.println(a+b);
			
		}
	}//main
}
